<code_news><h2><marquee >Nuevo Servicio - Constancia de Retenciones</marquee></h2>
<b style="font-size:14px">La Tesorería General de la Provincia</b>  cuenta con un nuevo servicio para la 
<b>Impresion de Constancias de Retenciones a las Ganancias,  
Contribuciones Patronales e Ingresos Brutos</b>..
Este servicio quedara en vigencia a partir del dia 20 de Noviembre del corriente año.
Para mas informacion haga clic <a href="tutoriales/retenciones.pdf" target="_blank" style="text-decoration: none">aqui</a>.</h3>
</code_news>
<h1>Bienvenido!</h1>
<!-- <div class="descr">Jueves, 22 de Mayo de 2008.</div> -->
<blockquote>
<p>Lo invitamos a formar parte de este nuevo sistema, el cual responde a los objetivos de perfeccionamiento, transparencia y modernizaci&oacute;n del Sector P&uacute;blico.</p>
<p>A través de este sistema Ud. podrá ser parte de la nueva Base de Datos de BENEFICIARIOS DEL ESTADO, condición inevitable para poder recibir cualquier tipo de acreencia por parte de la Provincia.</p>
</blockquote>
<p>A tal efecto solo debe seguir las siguientes instrucciones:</p>
<h4>1. Registración.</h4>
<br />
<p><b>Opción a) Vía Web:</b> mediante <a href="http://contaduria.larioja.gov.ar/beneficiarios" style="text-decoration: none">http://contaduria.larioja.gov.ar/beneficiarios</a> Ud, podr&aacute; ingresar a BENEFICIARIOS y seleccionar seg&uacute;n corresponda PERSONA F&Iacute;SICA y/o JUR&Iacute;DICA (para m&aacute;s informaci&oacute;n dirijase a <a href="http://www.larioja.gov.ar/beneficiarios/tutoriales/tutorial.pdf" style="text-decoration: none">Tutorial</a>).</p>
<p><u>Importante</u>: Para poder imprimir o visualizar la Constancia de Carga y/o el <a href="http://www.larioja.gov.ar/beneficiarios/tutoriales/tutorial.pdf" style="text-decoration: none">Tutorial</a>, su equipo deber&aacute; tener instalado alguna aplicaci&oacute;n que le permita trabajar con documentos PDF, como el Adobe Reader (el mismo, lo puede bajar desde su pagina oficial <a href="http://get.adobe.com/es/reader/" style="text-decoration: none">www.adobe.com</a>).</p>
<p>Este sitio recomienda el uso del navegador Mozilla Firefox, el mismo lo puede descargar desde su p&aacute;gina oficial <a href="http://www.mozilla.com/es-AR/" style="text-decoration: none">www.mozilla.com</a></p>
<p><b>Opci&oacute;n b) V&iacute;a Tesorer&iacute;a General de la Provincia:</b> para lo cual deber&aacute; concurrir a las oficinas de dicho &Oacute;rgano sito en la Calle 25 de Mayo y San Nicol&aacute;s de Bari (Oeste), 2do. Piso (Casa de Gobierno) en horarios de 8:30 a 12:00 hrs., con la documentaci&oacute;n original correspondiente (ver punto 5) para que el personal a cargo proceda con dicha registraci&oacute;n.</p>
<h4>2. Impresi&oacute;n de la Constancia de Carga.</h4>
<br />
<p>Si Ud. opt&oacute; por la opci&oacute;n V&iacute;a Web, una vez cargados sus datos debe hacer clic en ENVIAR DATOS y luego en la opci&oacute;n SIGUIENTE, aparecer&aacute; la leyenda &quot;Si desea imprimir por favor haga clic aqui&quot;, realice dicha acci&oacute;n, luego se mostrar&aacute; en pantalla su constancia de carga (4 hojas; 2 originales y 2 copias), proceda a imprimir las mismas.</p>
<h4>3. Presentaci&oacute;n de la Constancia de Carga en la Tesorer&iacute;a General de la Provincia.</h4>
<br />
<p>Una vez conclu&iacute;do el punto anterior, Ud. tendr&aacute; en su poder cuatro hojas, las cuales deber&aacute;n ser presentadas por el TITULAR en la Tesorer&iacute;a General de la Provincia a efectos de realizar la firma de las mismas ante el personal de dicho &Oacute;rgano. A tal efecto, es importante aclarar que deber&aacute; presentarse en dicha ocasi&oacute;n con la documentaci&oacute;n original correspondiente (ver punto 5).</p>
<h4>4. Alta como Beneficiario.</h4>
<br />
<p>Una vez aprobada la presentación por el personal a cargo de la Tesorería General de la Provincia, será el momento a partir del cual Ud. habrá ingresado como Beneficiario al Sistema de Administración Financiera.</p>
<h4>5. Documentaci&oacute;n requerida.</h4>
<br />
<p>La Documentaci&oacute;n que se solicita, deber&aacute; ser en original y/o copia certificada y legalizada seg&uacute;n corresponda y consiste en:</p>
<p><b>Personas F&iacute;sicas</b></p>
<ul>
	<li>Original y Copia del D.N.I. (2 primeras hojas).</li>
	<li>Original y Copia de Formulario Inscripci&oacute;n en la A.F.I.P.</li>
	<li>Original y Copia de Formulario Inscripci&oacute;n en la D.G.I.P.</li>
	<li>Constancia de C.U.I.L./C.U.I.L.</li>
	<li>Certificado original de cuenta bancaria que contenga número, tipo y C.B.U de la cuenta bancaria de su titularidad y elegida para opera.</li>
</ul>
<p><b>Personas Jur&iacute;dicas</b></p>
<ul>
	<li>Original y Copia de D.N.I. del Socio Gerente, Presidente, etc. seg&uacute;n corresponda.</li>
	<li>Original y Copia de Formulario Inscripci&oacute;n en la A.F.I.P.</li>
	<li>Original y Copia de Formulario Inscripci&oacute;n en la D.G.I.P.</li>
	<li>Copia certificada del Estatuto o Contrato Social.</li>
	<li>Copia certificada del Acta de Designaci&oacute;n de Autoridades.</li>
	<li>Certificado original de cuenta bancaria que contenga número, tipo y C.B.U de la cuenta bancaria de su titularidad y elegida para opera.</li>
	<li>Constancia de Inscripci&oacute;n Organismo de Control correspondiente actualizada.</li>
    <li>Declaraci&oacute;n Jurada del Domicilio Legal.</li>
    <li>Mandatarios con Poder.</li>
</ul>
<p><u>Nota aclaratoria:</u> Toda persona que act&uacute;e por mandato deber&aacute; acompa&ntilde;ar el respectivo poder con facultades suficientes, certificado por Escribano P&uacute;blico y legalizado si es de extra&ntilde;a jurisdicci&oacute;n, al igual que la documentaci&oacute;n exigida seg&uacute;n corresponda.</p>

